let currentIndex = 0;

startBtn.addEventListener('click', () => {
  bgMusic.play().catch(error => console.log('Error playing audio:', error));
  journeyContent.classList.remove('hidden');
  startBtn.classList.add('hidden');
  showMessage();
});

nextMessageBtn.addEventListener('click', () => {
  currentIndex++;
  if (currentIndex < messages.length) {
    showMessage();
  } else {
    finalMessageEl.classList.remove('hidden');
    journeyContent.classList.add('hidden');
  }
});

function showMessage() {
  const { pic, message } = messages[currentIndex];
  imageEl.src = pic;
  messageEl.innerHTML = message; // IMPORTANT: use innerHTML not textContent
}
